export default {
    somar: function(valor1, valor2) {
        return valor1 + valor2;
    },

    subtrair: function(valor1, valor2) {
        return valor1 - valor2;
    },
    
    multiplicar: function(valor1, valor2) {
        return valor1 * valor2;
    },

    dividir: function(valor1, valor2) {
        return valor1 / valor2;
    }
}